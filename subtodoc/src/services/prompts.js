/**
 * buildPrompt — AI에게 보낼 최종 지시문을 만들어주는 함수
 *
 * 마치 편지를 쓸 때 "받는 사람:", "내용:", "추신:" 처럼
 * 여러 정보를 합쳐서 하나의 완성된 지시문을 만들어요.
 *
 * @param {string} formatId        - 어떤 형식으로 만들지 (예: 'summary', 'blog')
 * @param {string} language        - 어떤 언어로 쓸지 (기본값: '한국어')
 * @param {object} options         - 추가 옵션들
 * @param {boolean} options.includeTimestamps  - 시간 표시([01:23])를 넣을지 여부
 * @param {string}  options.customInstruction  - 사용자가 직접 입력한 특별 지시사항
 * @returns {string} - AI에게 보낼 완성된 지시문 문자열
 *
 * 예시 결과:
 * "다음 YouTube 영상 자막을 한국어로 변환해줘.
 *  핵심 내용을 불릿 포인트로 간결하게 요약해줘.
 *  중요한 내용마다 가까운 타임스탬프를 [MM:SS] 형식으로 포함해줘.
 *
 *  자막:"
 */
export function buildPrompt(formatId, language = '한국어', options = {}) {
  // 옵션에서 타임스탬프 포함 여부와 사용자 지시사항을 꺼내요
  // 없으면 기본값(false, 빈 문자열)을 써요
  const { includeTimestamps = false, customInstruction = '' } = options

  // FORMATS 목록에서 사용자가 선택한 형식을 찾아요
  const format = FORMATS.find(f => f.id === formatId)

  // 기본 지시문을 만들어요
  // → "다음 YouTube 영상 자막을 한국어로 변환해줘. 핵심 내용을 불릿 포인트로..."
  let prompt = `다음 YouTube 영상 자막을 ${language}로 변환해줘.\n${format.instruction}\n`

  // 타임스탬프 포함을 켰다면 추가 지시를 붙여요
  // → AI가 "[01:23] 핵심 내용" 형태로 시간 표시를 넣어줘요
  if (includeTimestamps) {
    prompt += `중요한 내용마다 가까운 타임스탬프를 [MM:SS] 형식으로 포함해줘. 예: [01:23] 핵심 내용\n`
  }

  // 사용자가 직접 지시사항을 입력했다면 그것도 붙여요
  // → 예: "초보자 친화적으로", "유머러스하게"
  if (customInstruction.trim()) {
    prompt += `추가 지시사항: ${customInstruction.trim()}\n`
  }

  // 마지막에 "자막:" 을 붙여서 실제 자막 텍스트가 이어질 자리를 만들어요
  return prompt + '\n자막:\n'
}
